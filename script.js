function soma() { 
  let p = prompt('Digite um número:') 
  let p2 = prompt('Digite outro número:') 
  return alert(parseFloat(p)+parseFloat(p2)) 
} 

function subtrair() { 
  let pergunta = prompt('Digite um número:') 
  let pergunta2 = prompt('Digite outro número:') 
  return alert(pergunta-pergunta2) 
} 

function multiplicar() {
  let pergunta = prompt('Digite um número:')
  let pergunta2 = prompt('Digite outro número:') 
  return alert(pergunta*pergunta2)
}

function dividir() {
  let pergunta = prompt('Digite um número:') 
  let pergunta2 = prompt('Digite outro número:')
  return alert(pergunta/pergunta2) 
}
